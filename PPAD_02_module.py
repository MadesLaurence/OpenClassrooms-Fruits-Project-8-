def standardscaler(df, col_list):
    '''
       Standard Scaler sur les colonnes de col_list
        
            df : dataframe
            col_list : liste des colonnes à standardiser

            return : dataframe enrichi des colonnes standardisées
    '''    
    from sklearn.preprocessing import StandardScaler
    import pandas as pd

    dico = {}
    standardscale = StandardScaler().fit(df[col_list])
    df_std = pd.DataFrame(standardscale.transform(df[col_list]))
    for i in range(len(col_list)): dico[i] = col_list[i]
    df_std.rename(columns = dico, inplace = True)
    df.drop(columns=col_list, inplace = True)
    df.reset_index(drop = True, inplace = True)
    df = pd.concat([df, df_std], axis = 1)
    
    return df

def graph_eboulis_valeurspropres (pca_var, size_var, with_graph):
    '''
       Dessine la variance expliquée absolue et cumulée sur 2 graphes côte côte
        
            pca_var : pca après entraînement
            size_var : dimensions du graphe
            with_graph : avec ou sans représentatnio graphique
            
            return : dataframe contenant la variance expliquée absolue et cumulée 
    ''' 
    import matplotlib.pyplot as plt
    import pandas as pd
    
    df_eboul = pd.DataFrame(pca_var.explained_variance_ratio_, columns=['explained_variance_ratio'])
    df_eboul.reset_index(drop = False, inplace = True)
    df_eboul.rename(columns={'index':'rang'}, inplace = True)
    df_eboul['rang'] += 1
    df_eboul['explained_variance_ratio_cum'] = df_eboul['explained_variance_ratio'].cumsum()

    if with_graph:
        fig = plt.figure(figsize=(size_var))
    
        ax = fig.add_subplot(2, 2, 1)
        plt.plot(df_eboul['rang'], df_eboul['explained_variance_ratio'], \
                             label = 'explained_variance_ratio', color='blue')
        plt.annotate(str(df_eboul['explained_variance_ratio'].iloc[0]), \
                            (10, df_eboul['explained_variance_ratio'].iloc[0]))
        ax.set_title('Eboulis des valeurs propres', fontsize=12)
        ax.set_xlabel("Rang")
        ax.set_ylabel("explained_variance_ratio")       
    
        ax = fig.add_subplot(2, 2, 2)
        plt.plot(df_eboul['rang'], df_eboul['explained_variance_ratio_cum'], \
                             label = 'explained_variance_ratio_cum',color='green')       
        ax.set_title('Eboulis des valeurs propres cumulées', fontsize=12)
        ax.set_xlabel("Rang")
        ax.set_ylabel("explained_variance_ratio_cum")       
        
        plt.legend()
        plt.show()
    
    return df_eboul
